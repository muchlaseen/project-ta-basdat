    private void showTableClientData() throws SQLException {
        DefaultTableModel dtmClientData = new DefaultTableModel(new String[]{"CLIENT ID", "NAME", "ADDRESS", "EMAIL", "PHONE NO"}, 0);
        dtmClientData.setRowCount(0);
        for (Client cl : this.controller.getClientData()) {
            dtmClientData.addRow(new String[]{cl.getClId().toString(), cl.getClName(), cl.getClAddr(), cl.getClEmail(), cl.getClPhoneNo()});
        }

        tbClientData.setModel(dtmClientData);
    }

