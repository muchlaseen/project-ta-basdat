    private void btnAddClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddClientActionPerformed
        // TODO add your handling code here:
        try {
            Client cl = new Client();
            cl.setClId(Integer.parseInt(this.txtClientId.getText()));
            cl.setClName(this.txtClientName.getText());
            cl.setClAddr(this.txtClientAddr.getText());
            cl.setClEmail(this.txtClientEmail.getText());
            cl.setClPhoneNo(this.txtClientPhone.getText());
            this.controller.insertClient(cl);
            this.showTableClientData();
            
        } catch (SQLException ex) {
            Logger.getLogger(EntryClientView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddClientActionPerformed

    private void btnUpdateClActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateClActionPerformed
        // TODO add your handling code here:
        try {
            String selected = this.tbClientData.getValueAt(this.tbClientData.getSelectedRow(), 0).toString();
            Client cl = new Client();

            cl.setClId(Integer.parseInt(selected));
            cl.setClName(this.txtClientName.getText());
            cl.setClAddr(this.txtClientAddr.getText());
            cl.setClEmail(this.txtClientEmail.getText());
            cl.setClPhoneNo(this.txtClientPhone.getText());
            this.controller.updateClient(cl);
            this.showTableClientData();

        } catch (SQLException e) {
            Logger.getLogger(EntryClientView.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnUpdateClActionPerformed

    private void btnDelClActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelClActionPerformed
        // TODO add your handling code here:
        try {
            String selected = this.tbClientData.getValueAt(this.tbClientData.getSelectedRow(), 0).toString();
            this.controller.deleteClient(Integer.parseInt(selected));
            this.showTableClientData();

        } catch (SQLException e) {
            Logger.getLogger(EntryClientView.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnDelClActionPerformed

