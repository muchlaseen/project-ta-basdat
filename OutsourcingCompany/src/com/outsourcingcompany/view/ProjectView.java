    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Project project = new Project();
        try {
            project.setJob(this.assignment.getJobData().get(this.cbJobId.getSelectedIndex()));
            project.setEmployee(this.assignment.getEmployeeData().get(this.cbEmpId.getSelectedIndex()));
            project.setStartDate(new SimpleDateFormat("dd/MM/yyyy").parse(this.txtStartDate.getText()));
            project.setDueDate(new SimpleDateFormat("dd/MM/yyyy").parse(this.txtDueDate.getText()));
            this.assignment.insertProject(project);
            
            JOptionPane.showMessageDialog(null, "Project added!");
            this.showTableProject();
            
        } catch (SQLException | ParseException err) {
            System.out.println(err);
        }

    }//GEN-LAST:event_btnAddActionPerformed

