    private void showComboBoxEmployee() throws SQLException {
        DefaultComboBoxModel dtmEmployee = new DefaultComboBoxModel();
        for (Employee emp : this.assignment.getEmployeeData()) {
            dtmEmployee.addElement(emp.getEmpId());
        }

        this.cbEmpId.setModel(dtmEmployee);
    }

    private void showComboBoxJob() throws SQLException {
        DefaultComboBoxModel dtmJob = new DefaultComboBoxModel();
        for (Job j : this.assignment.getJobData()) {
            dtmJob.addElement(j.getJobId());
        }

        this.cbJobId.setModel(dtmJob);

    }

    private void showTableJob() throws SQLException {
        DefaultTableModel dtmJobList = new DefaultTableModel(new String[]{"JOB ID", "JOB NAME", "DESCRIPTION", "CLIENT NAME"}, 0);
        dtmJobList.setRowCount(0);
        for (Job j : this.assignment.getJobClientData()) {
            dtmJobList.addRow(new String[]{j.getJobId().toString(), j.getJobName(), j.getJobDesc(), j.getClient().getClName()});
        }

        tbOutstandingJob.setModel(dtmJobList);
    }

    private void showTableProject() throws SQLException {
        DefaultTableModel dtmProjectList = new DefaultTableModel(new String[]{"PROJECT ID", "JOB NAME", "EMPLOYEE NAME", "START DATE", "DUE DATE"}, 0);
        dtmProjectList.setRowCount(0);
        for (Project p : this.assignment.getProjectData()) {
            dtmProjectList.addRow(new String[]{p.getPrjId().toString(), p.getJob().getJobName(), p.getEmployee().getEmpName(), p.getStartDate().toString(), p.getDueDate().toString()});
        }

        tbOnProgressProject.setModel(dtmProjectList);
    }

    private void showTableEmployee() throws SQLException {
        DefaultTableModel dtmEmpList = new DefaultTableModel(new String[]{"EMPLOYEE ID", "NAME", "ADDRESS", "GENDER", "PHONE NO", "EMAIL"}, 0);
        dtmEmpList.setRowCount(0);
        for (Employee e : this.assignment.getEmployeeData()) {
            dtmEmpList.addRow(new String[]{e.getEmpId().toString(), e.getEmpName(), e.getEmpAddr(), e.getEmpGender(), e.getEmpPhoneNo(), e.getEmpEmail()});

        }

        tblEmployeeList.setModel(dtmEmpList);
    }

